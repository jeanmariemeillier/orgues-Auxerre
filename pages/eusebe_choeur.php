		 <BR>
		 Deuxième clavier :
		 <BR>
		 <BR>
		 5. Viole de gambe 8'
		 <BR>
		 6. Voix céleste 8'
		 <BR>
		 7. Trompette harmonique 8'
		 <BR>
		 <BR>
		 Pédale :
		 <BR>
		 <BR>
		 8. Soubasse 16'
		 <BR>
		 <BR>
		 Pédales de combinaisons et sous les tirants de jeux 8 boutons de combinaisons.
		 <BR>
		 <BR>
		 Le prix de l'orgue serait de 4000 francs.
