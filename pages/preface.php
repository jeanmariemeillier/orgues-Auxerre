		   <B>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;"Lorsque nous nous trouvons en présence d'un 
		   monument s'imposant à notre admiration, comme la cathédrale d'Auxerre, autant par la pureté de ses grandes 
		   lignes architecturales que par le fini et la richesse de ses sculptures, notre ardent désir n'est-il pas 
		   d'interroger son passé, de rechercher les noms des habiles ouvriers et des hauts personnages qui ont
		   contribué à sa construction et à son ornementation, enfin de connaître les événements dont il a pu être le 
		   théatre ? Cet ouvrage qui pourrait satisfaire notre légitime curiosité, n'existe pas ; la monographie de 
		   ce splendide édifice, la gloire de la ville d'Auxerre, qui ne le cède pas en beauté à ce que l'art ogival
		   a produit de plus remarquable, est encore à faire... En attendant que cette regrettable lacune soit comblée,
		   force nous est de nous contenter des documents épars consignés sur ce monument dans la Grande Histoire
		   d'Auxerre de l'Abbé LEBEUF.<font color="#ff0000">*</font> 
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quant à moi, reconnaissant mon insuffisance
		   pour mener à bien un pareil travail, je me bornerai à apporter ma modeste contribution à celui qui
		   l'entreprendra un jour, en relatant à l'aide de documents déposés aux Archives de l'Yonne, les différents
		   travaux exécutés dans l'église..."
		   <br>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
