		   <br>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;En ce qui me concerne, ma participation portera
		   uniquement sur les orgues de la Cathédrale et ce qui y est, plus ou moins, directement associé.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Seule dérogation à cette affirmation, quelques 
		   traits d'histoire afin de situer l'environnement des orgues.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Enfin, et pour conclure cette introduction, je
		   tiens à préciser que je ne suis pas écrivain, et ne dispose d'aucune des facultés propres à cet art ; que
		   la réalisation de cet ouvrage, n'est que la compilation de recherches effectuées par des auteurs aux noms 
		   célèbres.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Le texte peut paraître décousu et les styles variés. 
		   La raison en est simple : je n'ai pas eu l'indélicatesse de reprendre à mon compte toutes les citations et,
