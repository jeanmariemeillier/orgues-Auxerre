			<P ALIGN="CENTER"><FONT SIZE=5><B>LA CATHEDRALE</B></FONT></P>
			<CENTER><B>Bulletin Paroissial de Saint Etienne d'Auxerre</B></CENTER>
			<CENTER><B>paraissant tous les deux mois</B></CENTER>			  	 	 
