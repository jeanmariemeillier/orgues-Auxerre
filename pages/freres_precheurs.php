<h1 class="cnt">Les Frères Prêcheurs</h1>
<p>
	Le couvent des Frères Prêcheurs, ou Jacobins, ou Dominicains, abrite aujourd'hui les locaux de la Trésorerie générale. Les bâtiments sont tout à fait modernisés.
</p>
