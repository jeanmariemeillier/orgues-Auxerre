		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Après Annibal GANTEZ, vint Jean CATHALA qui fut 
		   maître de musique à l'église cathédrale d'Auxerre, vers la fin du XVIIème siècle (Fétis, Dict.biog. V J.Cathala).
		   C'était aussi un homme distingué, dont on a conservé plusieurs compositions parmi lesquelles cinq messes. L'une 
		   de ces dernières ne contient pas une seule note blanche, et porte pour épigraphe : (Nigra sum ; sed formosa). On 
		   voit que GANTEZ n'avait pas seul le monopole des bizarreries musicales.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(On a peine à croire combien la musique religieuse 
		   était soigneusement cultivée à Auxerre, dans le XVIIème siècle, au temps de GANTEZ et de CATHALA. <font size=+1>
		   <b>Il y avait des orgues dans presque toutes les grandes églises </b></font>: à St Etienne, à St Pierre, à St 
		   Eusèbe, à St Germain, à St Marien. Il y en avait même dans les abbayes, aux Jacobains, par exemple. Celles de 
		   St Marien, construites en 1639, furent refaites dès 1677, par des facteurs de Troyes.)
		   <br>
		   <br>
		   
		   <font color="#ff0000">**</font><font color="#666600"><i>Il est dédié à Pierre de BROC, et l'auteur explique 
		   lui-même à l'évêque les motifs de sa publication et de sa dédicace.</i></font>
		   <br>
		   <font color="#666600"><i>"Monseigneur, ce n'est pas vanité d'exposer au public que j'ai composé ce petit traité ;</i></font>
		   <br>
		   <font color="#666600"><i>"mais pour éviter l'oisiveté, laquelle j'estime si dangereuse, que j'aimerais mieux dormir</i></font>
		   <br>
		   <font color="#666600"><i>"(ainsi que disait un gentilhomme bourguignon), que de ne rien faire... néanmoins,</i></font>
		   <br>
		   <font color="#666600"><i>"parce que les premières de toutes choses sont dues à Dieu et à ses lieutenants en terre,</i></font>
		   <br>
		   <font color="#666600"><i>"je ne sçaurais, monseigneur, esviter de l'offrir à votre grandeur, puisque vous êtes</i></font>
		   <br>
		   <font color="#666600"><i>"son pasteur et bienfaiteur et que je suis votre créature, par un bénéfice que votre bonté</i></font>
		   <br>
		   <font color="#666600"><i>"vient tout fraichement de me donner. D'ailleurs, après avoir considéré que ce livre s'adresse</i></font>
		   <br>
		   <font color="#666600"><i>"aux chantres, il m'a semblé ne pouvoir rencontrer un meilleur protecteur, puisque vous avez</i></font>
		   <br>
		   <font color="#666600"><i>"un si grand amour pour les musiciens, que "presque toute votre maison en est composée."</i></font>
		   <br>
		   <br>
		   
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Nous arrivons maintenant au XVIIIème siècle, et tout 
		   d'abord nous rencontrons le nom de l'Abbé LEBEUF. A Dieu ne plaise que je retrace ici les détails d'une vie bien 
		   connue : cette tâche a été accomplie par deux de nos collègues, d'une façon qui ne laisse rien à désirer.
		   Quelques mots seulement sur les services que l'abbé LEBEUF a rendus à la musique.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;On sait qu'il avait étudié le plain-chant à l'église 
		   de St Regnobert, où il se format à l'état ecclésiastique. Plus tard, il reçut à Paris les leçons et les conseils 
		   de l'Abbé CHASTELAIN. A peine âgé de 16 ans, il fut appelé dans le diocèse de Lisieux pour y réformer les chants 
		   sacrés, et il acquit, dans cette &#156;uvre difficile une immense réputation.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Aussi, quand M de VINTIMILLE voulut donner à son 
		   église un nouvel antiphonier, ce fut à l'abbé LEBEUF qu'il s'adressa. Son travail, commencé en 1734, fut publié, 
		   dès 1736, bien qu'il comportât 5 volumes in-8°.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Il suffit de lire certains écrits de l'abbé LEBEUF, 
		   pour s'assurer combien la musique moderne avait flatté son esprit, et combien cette influence a du nuire, dans 
		   son antiphonier, à l'exacte reproduction du plain-chant primitif.
		   <br><i>"Ce serait </i>dit-il <i>une injustice de ne pas reconnaître que</i>
		   <br>
		   <i>"le goût supérieur de la musique d'aujourd'hui fait naître, dans</i>
		   <br>
		   <i>"l'esprit de ceux qui enfantent du plain-chant, de certains pro-</i>
		   <br>
		   <i>"grès de voix et de certaines mélodies qui ont leur douceur par-</i>
		   <br>
		   <i>"ticulières ; qu'il y a des tours gracieux qui ne peuvent être sug-</i>
		   <br>
		   <i>"gérés que par des organes qui ont été souvent rabattus de sons a-</i>
		   <br>
		   <i>"gréables et affectueux."</i> (Traité hist. et prat. Du plain-chant).
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;De cette phrase résulte évidemment que l'abbé LEBEUF 
		   n'a pas toujours compris la différence profonde qui doit séparer le plain-chant de la musique moderne. L'un n'a 
		   rien à gagner au progrès de l'autre. Chacun a son caractère, ses règles, sa tonalité ; et le but des véritables 
		   artistes doit être de maintenir une distinction aussi essentielle, plutôt que de l'effacer par une confusion 
		   fâcheuse.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;On a aussi reproché à l'abbé LEBEUF d'avoir nui à la 
		   popularité du plain-chant. C'est qu'en effet, au lieu de conserver les mélodies syllabiques des anciens 
		   compositeurs, qui se distinguent par leur simplicité, leur naïveté, et se gravent aisément dans toutes les 
		   mémoires, il les a surchargées de notes, de tirades qui les alourdissent et en détruisent l'originalité.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quel a été le motif d'une telle altération ? 
		   LEBEUF nous l'explique par ces mots :
		   <br>
		   <i>"Ceux qui voudront dire la vérité fondée sur l'expérience,</i>
		   <br>
		   <i>"conviendront qu'il est plus facile de faire rouler la voix, et</i>
		   <br>
		   <i>"s'accorder à l'unisson dans les pièces un peu chargées de notes</i>
		   <br>
		   <i>"et de tirades à degrés conjoints, que dans les pièces notées</i>
		   <br>
		   <i>"syllabiquement d'un bout à l'autre."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ainsi l'abbé LEBEUF s'est préoccupé des difficultés 
		   d'exécution. Il a sacrifié la musique aux commodités des chanteurs. Mais en cela même, il s'est trompé. Car, 
		   dans la musique d'église, les chanteurs ce sont tous les assistants, et, en général, les masses ne retiennent 
		   bien que les mélodies simples et sans tirades. Toutes les mélodies populaires sont là pour en faire foi.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Je ne reproduis ces diverses critiques que parce 
		   qu'elles sont aujourd'hui consacrées par la science, et que la gloire de notre illustre compatriote est encore 
		   assez belle pour n'en point souffrir.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Elles ont été formulées par Dom GUERANDEZ et par
		   M DAUJOU, savant organiste, qui a passé sa vie à étudier les anciens monuments de la musique sacrée, et qui a 
		   découvert l'antiphonaire grégorien de Montpellier.
		   <br>
		   <i>L'abbé LEBEUF </i>dit-il (Revue de la musique religieuse, mai
		   <br>"1846)<i> sur l'invitation de M. de VINTIMILLE, archevêque de Paris,</i>
		   <br>
		   <i>"eut le courage de compléter l'oeuvre de destruction commencée par</i>
		   <br>
		   <i>"l'abbé CHASTELAIN et de recomposer un nouveau chant qui fut fa-</i>
		   <br>
		   <i>"briqué en peu d'années. L'abbé LEBEUF était un homme instruit, le</i>
		   <br>
		   <i>"plus instruit peut-être de ceux qui ont ensuite imité son vanda-</i>
		   <br>
		   <i>"lisme ; mais la science même qu'il possédait est une circonstance</i>
		   <br>
		   <i>"aggravante de plus dans le procès que la postérité lui intente,</i>
		   <br>
		   <i>"et qui se terminera, s'il plait à Dieu, par une condamnation sans</i>
		   <br>
		   <i>"appel."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Au reste, les fautes commises par l'abbé LEBEUF sont 
		   plutôt imputables à son époque qu'à lui-même. Le XVIIIème siècle ne se prêtait guère à ces restaurations 
		   consciencieuses, si fréquentes de nos jours, où l'artiste moderne s'efface et s'annule derrière l'artiste 
		   ancien, pour faire revivre la pensée de celui-ci dans toute son intégrité. C'était un siècle trop saillant, 
		   trop vigoureux, trop original, pour ne pas laisser son emprunte, bonne ou mauvaise, à tout ce qu'il touchait. 
		   Alors, un architecte chargé de réparer une église gothique y adaptait l'architecture en vogue, et tout le 
		   monde applaudissait à cet embellissement. L'abbé LEBEUF a fait de même pour la musique sacrée, il a suivi la 
		   pente générale, ou du moins il n'a pas su résister, autant qu'il l'eut voulu, à l'entraînement de son époque.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;La preuve de cet entraînement est que ses contemporains 
		   ont été bien moins sévères pour lui que pour les nôtres. Il eut de son temps la réputation de premier liturgiste
		   et du plus habile compositeur de musique sacrée : si bien, qu'
		   <br>
		   <i>"il ne s'est pas fait en France, </i>(disent ses biographes)<i>de</i>
		   <br>
		   <i>"1730 à 1760, un seul changement dans les livres de chant d'église</i>
		   <br>
		   <i>"sur lesquels il n'ait été consulté."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ajoutons, comme un détail de sa vie intime, qu'il se 
		   montrait fort jaloux de sa supériorité et de son influence. Un sieur Cousin de COUTAMINE, ayant fait paraître, 
		   en 1749, un livre sur le plain-chant (dédié à l'abbé POISSON), y attaqua les doctrines musicales de l'abbé 
		   LEBEUF et fit même précéder son ouvrage d'une vignette allégorique représentant un boeuf piqué sur un cousin. 
		   Notre illustre compatriote fut très offensé d'une pareille attaque, et il en écrivit à l'auteur avec la plus 
		   grande vivacité.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ce qui, d'après nous, constitue le principal titre 
		   de l'abbé LEBEUF à l'estime et à l'admiration des musiciens, ce sont ses innombrables recherches, dont le 
		   Mercure de France conserve mille traces ; ce sont ses dissertations sur l'état des sciences et des arts au 
		   moyen-âge ; c'est surtout son traité historique et pratique du plain-chant (il paraît, cependant, que la partie 
		   historique est seule due à l'abbé LEBEUF ; la partie didactique est de l'abbé CHASTELAIN. LEBEUF n'a fait que 
		   la revoir et l'éditer).
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quel que soit le savoir des historiens récents de 
		   la musique, ils en sont toujours réduits à citer l'abbé LEBEUF. On peut s'en assurer en lisant les travaux de 
		   M. FETIS ou ceux de M. DANJOU. A chaque pas, ils s'appuient de l'opinion d'un homme qu'ils critiquent parfois, 
		   ainsi que nous l'avons vu, mais qu'en définitive ils respectent.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;D'après M. FETIS, son traité historique est, avec 
		   celui du P. JUMELHAC et celui de l'abbé POISSON
		   <br>
		   <i>"ce qu'on a publié de meilleur en France sur le plain-chant."</i><font color="#ff0000">***</font>
		   <br>
		   Un pareil éloge dispense de tout commentaire.
		   <br>
		   <br>
		   <font color="#ff0000">***</font><font color="#666600"><i>V. FETIS, Dict. V° Léonard POISSON.-M. FETIS indique 
		   seulement la date de naissance de l'abbé POISSON, et en omet le lieu. L'abbé POISSON est né à Cerisiers. 
		   (Registre des collations de Bénéfice, archevêché de Sens. Lettres de tonsure du 18 décembre 1716).</i></font>
		   <br>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Après la mort de l'abbé LEBEUF, et jusqu'à la
		   révolution française, l'Eglise d'Auxerre ne produisit plus aucun musicien célèbre. Néanmoins, la musique 
		   sacrée continue d'être en singulier honneur.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Deux états dressés, l'un en mars 1767, l'autre 
		   en octobre 1790, constatent que la chapelle de musique était la principale charge du chapitre d'Auxerre.<font 
		   color="#ff0000">****</font>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;On peut lire dans le dernier de ces états les noms 
		   et les services de ceux qui la composaient, lorsque la révolution vint dissoudre le chapitre et les institutions 
		   qui s'y rattachaient.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ces noms modestes, ignorés maintenant, terminent la 
		   liste des hommes qui dans notre pays, consacrèrent leur vie au culte de la musique religieuse. Sous ce rapport, 
		   ils sont dignes d'un certain intérêt, et j'ai cru devoir les transcrire, tels que les a mentionnés M. ASSELINE, 
		   chanoine, secrétaire du chapitre :
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
		   "Bas choeur"
		   <br>
		   &#149; Edme CHAPPOTIN, maître de musique est au service de la compagnie depuis 1734. Agé de 66 ans ;
		   <br>
		   &#149; <font size=+1><b>Joseph PALAIS</b></font>, organiste depuis 1734. Agé de 85 ans ;<font color="#ff0000">
