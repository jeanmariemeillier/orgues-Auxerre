		   <br>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Lorsque le moyen-âge finit, depuis longtemps le 
		   besoin d'une réforme liturgique avait frappé tous ceux qui cultivaient la musique sacrée. On profita, pour 
		   la tenter, de l'occasion qu'offrit alors la découverte de l'imprimerie. En effet, il était fort naturel de 
		   revoir les livres d'église et d'en corriger le chant avant de les livrer à l'impression.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;En 1552, le cardinal de BOURBON, archevêque de Sens, 
		   fit imprimer et publier l'antiphonier plénier de son église. Le soin des corrections musicales avait été
		   confié à un savant chanoine nommé Jean COUSIN ; mais, malgré ses efforts et malgré les éloges que ses 
		   contemporains lui prodiguèrent, son travail laissait beaucoup à désirer.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ceux qui, à Auxerre, retouchèrent les chants sacrés
		   furent encore moins habiles. L'abbé Léonard POISSON se plaint amèrement de leur goût baroque. Au lieu d'améliorer 
		   ce qui existait, ils contribuèrent à l'altérer d'avantage, et rendirent plus difficile la tâche de ceux qui
		   vinrent après eux.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vers ce temps, Jacques AMYOT, notre célèbre évêque, 
		   donnait aux études musicales une impulsion nouvelle. Très souvent, son palais épiscopal devint l'asile des 
		   artistes. Les historiens ajoutent que c'est aux concerts qui s'y donnaient, qu'Edmé GUILLAUME, chanoine
		   auxerrois, fit les premiers essais du serpent, dont il passe pour être l'inventeur. J'avoue que cette découverte
		   peu brillante, hélas, pourrait cependant nous être contestée car on trouve dans les comptes de la fabrique de
		   l'archevêché de Sens, pendant les années 1453 et 1454, ces mots "Ressoudé le serpent de l'église et mis au point 
		   un lien de laiton, qui tient le livre...". Si le serpent existait à Sens, en 1450, il est difficile qu'Edmé
		   GUILLAUME l'aît inventé ici, cent ans plus tard.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Au XVIIème siècle, l'église d'Auxerre eut deux
		   maîtres de chapelle fort renommés. L'un d'eux surtout, Annibal GANTEZ mérite de fixer l'attention par son
		   originalité. Il s'est chargé de nous raconter lui-même son histoire.
		   <br>
		   <i>"J'étais</i>, dit-il,<i> parti de Marseille </i>(son pays) <i>tout plein de</i>
		   <br>
		   <i>"bonne opinion, car le proverbe étant que les provençaux sont les</i>
		   <br>
		   <i>"plus naturels médecins et musiciens, je croyais faire la leçon à</i>
		   <br>
		   <i>"un chacun et enseigner Minerve. Mais je vous assure que j'ai bien</i>
		   <br>
		   <i>"trouvé soulier à mon point et des gens qui ne se mouchaient pas</i>
		   <br>
		   <i>"du pied.</i>
		   <br>
		   <i>"Il faut avouer que ceux de notre pays ont bien plus d'air en leur</i>
		   <br>
		   <i>"musique, mais ceux de celui-ci ont plus d'art en la leur, encore</i>
		   <br>
		   <i>"qu'il me semble que l'un n'est pas bon sans l'autre ; car, en ma-</i>
		   <br>
		   <i>"riant l'art avec l'air, il y a de quoi contenter chacun."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;GANTEZ chercha longtemps à utiliser ses talents 
		   musicaux. Les savantes écoles du moyen-âge avaient été remplacées par les maîtrises. Chaque église avait son 
		   maître de chapelle, et parfois ce titre était fort lucratif et fort ambitionné. Aussi, les musiciens du temps 
		   couraient toute la province pour trouver une maîtrise importante. Ils allaient, le soir, demander l'hospitalité 
		   soit à leurs confrères, soit aux curés et aux chanoines ; puis, le lendemain, ils se remettaient en route. On
		   appelait cela vicarier.
		   <br>
		   <i>"Ah ! </i>s'écrie GANTEZ<i>, que c'est une pauvre chose de vicarier</i>
		   <br>
		   <i>"sans argent !... Ma bourse ayant failli, il m'a fallu coucher au</i>
		   <br>
		   <i>"serein crainte de laisser mon manteau au cabaret, et par ce moyen</i> 
		   <br>
		   <i>"faire le noviciat des filous, lesquels font coucher sous la cape</i>
		   <br>
		   <i>"du ciel ceux qui veulent être reçus dans leur bande, afin de les</i>
		   <br>
		   <i>"accoutumer à la fatigue et à l'incommodité. Dans cet état, ce ne</i>
		   <br>
		   <i>"fut pas les puces qui m'empéchèrent de dormir, mais faute de</i>
		   <br>
		   <i>"n'avoir soupé, étant impossible de reposer si le ventre n'est sa-</i>
		   <br>
		   <i>"tisfait."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Du reste, toutes ses aventures ne l'empéchaient pas 
		   de cultiver la musique. On en jugera par cette anecdote, qu'il raconte :
		   <br>
		   <i>"Après avoir déjeuné chez un curé, la pluie me saisit si fort</i>
		   <br>
		   <i>"dans les montagnes du Limousin, que je ne savais de quel bois</i>
		   <br>
		   <i>"faire flèche, ni à quel saint me recommander. Néanmoins, étant</i>
		   <br>
		   <i>"éloigné des retraites, j'eus recours au ciel ; et, après avoir</i>
		   <br>
		   <i>"dit toutes les prières que je savais par mémoire, je composai en</i>
		   <br>
		   <i>"musique un psaume de David qui me sembla venir à-propos.</i>
		   <br>
		   <i>"<b>Salvum me fac Dominus, quonium intraverunt aquos esque animam meam</b>"."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Après de nombreuses traverses, GANTEZ finit par
		   obtenir ce qu'il souhaitait, c'est-à-dire une bonne maîtrise.
		   <br>
		   <i>"En celà </i>dit-il <i>j'ai fait comme la palme et le laurier qui</i>
		   <br>
		   <i>"résistent à la tempête et comme le safran qui plus il est foulé,</i>
		   <br>
		   <i>"mieux il croît : Dieu m'ayant asisté, puisque je possède une des</i>
		   <br>
		   <i>"meilleures et des plus honorables maîtrises du royaume, qui est</i>
		   <br>
		   <i>"celle d'Auxerre."</i><font color="#ff0000">*</font>
		   <br>
		   <br>
	       <font color="#ff0000">*</font><font color="#666600"><i> La maîtrise d'Auxerre n'était pas seulement très 
		   honorable, elle offrait des avantages matériels, et GANTEZ ne les oublie pas.</i></font>
		   <br>
		   <font color="#666600"><i>"Entre les maîtrises, la première sorte est celle où l'on vit 'en communauté avec les 
		   prêtres', comme dans St Paul à Paris, Marseille, Arles, Aiguemortes et Carpentras. La seconde est celle 'où les 
		   enfants sont ici avec le maître', ici en communauté, comme à St Jacques-de-l'Hôpital à Paris, Valence, Grenoble 
		   et le Havre-de-Grace. La troisième est celle 'où les enfants sont nourris avec le maître par procureur', (et la 
		   meilleure est celle 'où le maître nourrit les enfants', comme à St Innocent de Paris, [Auxerre], Montauban, 
		   Avignon et autres)"</i></font>
		   <br>
		   <font color="#666600"><i>On conçoit, en effet, que dans ce dernier système le maître joignant en quelque sorte 
		   les fonctions d'économe à celles de maître de chapelle, devait retirer de ce cumul d'importants bénéfices.</i>
		   </font>
		   <br>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;GANTEZ pu alors se livrer en paix à son goût pour 
		   les lettres et pour les arts. Il y était encouragé par le patronage de l'évêque Pierre de BROC, grand amateur de 
		   musique, et par la fréquentation d'artistes distingués, tels qu'<b>Antoine DOREMIEUX</b>, <b>célèbre 
		   organiste</b>, <b>amené par Pierre de BROC </b>lui-même.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Il publia diverses compositions musicales et entre 
		   autres plusieurs messes. Mais ces compositions durent nécessairement se ressentir de l'esprit léger et 
		   aventureux de leur auteur. D'ailleurs, GANTEZ était peu scrupuleux : ce qu'il cherchait avant tout c'était le 
		   succès. Il composa une messe dans le temps que Louis XIII envoyait des secours à Candie. Pour mettre son &#156;uvre 
		   à la mode, il arrangea son "Kyrie eleison" sur l'air de la chanson :
		   <br>
		   <i>"Allons à Candie, allons"</i>, que l'on avait faite à cette occasion, et que tout le monde répétait sans 
		   cesse. Il publia aussi un petit livre ayant pour titre :
		</P>
