				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vers le milieu du IXème siècle, vivait à
				 l'abbaye Saint Germain d'Auxerre un moine nommé Héric qui, outre la prière liturgique, passait la majeure 
				 partie de son temps à professer. L'une des parties les plus importantes de son enseignement était la 
				 musique sacrée. Depuis saint Ambroise et saint Grégoire, la musique occupait une grande place dans le 
				 culte chrétien. Chaque abbaye s'efforçait de former des chanteurs et des musiciens distingués. C'est ainsi 
				 que le moine Héric composa le chant d'un office en l'honneur de saint Germain, si beau selon ses 
				 contemporains, qu'au Xème siècle on le chantait encore à Auxerre et qu'au XIIème siècle l'église d'Autun 
				 l'avait adopté en entier et en fit même usage pour la fête de saint Lazare. Un élève d'Héric, Rémy, lui 
				 aussi moine à saint Germain d'Auxerre, fonda à Paris des cours publics de théologie et de musique. Faut-il 
				 parler de ces noms à peu près oubliés aujourd'hui d'Hubald, Foulques ou saint Odon de Cluny ? Ces hommes 
				 sont pourtant les principaux représentants du grand mouvement artistique, particulièrement musical, qui 
				 s'accomplit en Europe et surtout en France, vers la fin du IXème siècle et le commencement du Xème siècle. 
				 C'est à cette époque que les études musicales se multiplient, s'universalisent avec un élan incontestable 
				 qui a frappé d'étonnement tous les historiens. L'enseignement musical s'élève à la hauteur d'un 
				 enseignement scientifique. Et c'est de la grande abbaye Saint Germain d'Auxerre que partit la plus notable 
				 impulsion et que le progrès se répandit dans toute l'europe occidentale.
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Selon l'historien Lebeuf, Hugues de Noyers, 
				 évêque d'Auxerre au XIIème siècle, doit être compté parmi les compositeurs de musique sacrée les plus 
				 influents. Ses biographes qui étaient aussi ses contemporains, disent de lui : "<i>Cantica componebat 
				 et cantus</i>" (Il composait chants et cantiques). Il en est de même pour Erard de Lézigne, évêque sous 
				 Philippe le Hardi, désigné comme grand amateur de musique sacrée, tandis qu'une certaine décadence se 
				 manifeste fin XIIème siècle avec Pierre de Corbeil, archevêque de Sens, qui composa la fameuse messe de 
				 l'Ane.
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;La bibliothèque d'Auxerre possède un missel 
				 auxerrois du XIIème siècle qui, bien que s'éloignant beaucoup de l'antiphonaire grégorien, prouve que la 
				 musique religieuse était en honneur chez nous à cette époque.
				 <br>
				 <p align="center">
				 <font size=+1><b>...</b></font>
				 </p>
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Dans les dernières année du XVIIIème siècle,
				 la chapelle d'Auxerre compte encore un nombreux personnel, vieilli pour la plupart dans la carrière 
				 musicale. Et vient la Révolution de 1789 qui, établissant à la cathédrale le culte de la déesse Raison, 
				 n'a pas d'hymne à entonner en l'honneur du nouveau rite.
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Le XIXème siècle maintient, cependant la 
				 flamme vacillante avec un certain M. Chenet qui publie en 1839 un petit traité musical qu'il intitule :
				 "<i>Le Maître de Musique</i>". C'est une méthode en trois parties, dans laquelle il a cherché à 
				 populariser l'étude de la musique en général, celle du plain-chant en particulier.
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Parallèlement, s'est développée, à cette 
				 époque, une musique dite populaire, c'est-à-dire celle que nous connaissons au travers des fanfares, 
				 harmonies ou chorales. Elle a vu ses premiers élans donnés par Eugène Delaporte, dès 1848. Dans son 
				 traité de "<i>La musique populaire dans l'Yonne</I>" de 1850 à 1870, Bernard Mameron écrit : "<i>Un 
				 immense espoir de voir la musique enfin reconnue chez nous et pratiquée avec ardeur se lève un peu 
				 partout</I>"... Des fêtes de la musique sont organisées et, depuis le Festival de musique organisé en 
				 1852 à Villeneuve-sur-Yonne, le sous-préfet de Sens écrira au préfet de l'Yonne à Auxerre : "<i>Je 
				 crois, Monsieur le préfet, qu'il y a un excellent parti à tirer des corps de musique. Propager les 
				 institutions musicales dans les campagnes serait un moyen d'adoucir les moeurs, d'éloigner les cultivateurs 
				 des cabarets et de les soustraire à la propagande du socialisme. Le Conseil Général ferait acte de bonne 
				 politique en votant un crédit destiné à l'encouragement des sociétés musicales</i>"...
				 <br>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Plus tard, en 1861, un certain Richet, dont le 
				 titre d'inspecteur de chant dans les écoles l'autorise à visiter les 250 classes du département, pourra 

				 écrire : "<i>Dans l'ensemble, l'enseignement du chant est satisfaisant.</i> <font size=+1><b>Les classes 
				 pourvues d'un orgue obtiennent de biens meilleurs résultats !</b></font> Et il préconise l'attribution de 
				 subventions pour <b>acheter des orgues</b> pour toutes les écoles du département.
