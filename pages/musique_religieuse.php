		   lesquels cette mention :
		   <br>
		   &#149; René PRUNELLE, musicien depuis 1758. Agé de 58 ans. Il eut du chapitre la somme de 1200 liv., il y a 
		   quelques années, et il est resté attaché à ladite église, dans laquelle il dessert encore, en la même qualité 
		   de musicien.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;On voit que la chapelle d'Auxerre, dans les 
		   dernière années du XVIIIème, comptait encore un nombreux personnel, vieilli pour la plupart dans la carrière 
		   musicale.<font color="#ff0000">*****</font>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Mais ce qui est surtout remarquable et ce qu'on 
		   ne peut trop signaler à l'émulation du clergé actuel, c'est le soin avec lequel cette chapelle avait été
		   composée et la surveillance dont elle était l'objet.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Les registres capitulaires qui sont venus jusqu'à 
		   nous ne fournissent presque pas de délibérations, où l'on ne s'occupe de la musique religieuse et des musiciens 
		   chargés de l'interpréter.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;S'agit-il de nommer une basse-taille ou une 
		   basse-contre, on s'enquiert de toutes parts, on prend des informations ; on essaie, et on n'admet le
		   sollicitant qu'après un long examen ; ensuite on l'admoneste, on tâche de le fortifier.
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Ainsi, j'ouvre le registre des délibérations 
		   capitulaires de l'année 1785<font color="#ff0000">******</font>. Un sieur CAMPENON de Vaux, qui figure encore 
		   sur l'état de 1790, demande à être admis comme basse-contre. Le 1er février, le chapitre ordonne que l'on 
		   prendra des renseignements sur ses moeurs et qu'on fera examiner sa voix et sa science dans le plain-chant. Le 
		   14 février, on décide qu'on le fera venir de Paris où il se trouve alors. Cependant, si on n'est pas content, on 
		   se réserve de le renvoyer. Le 4 mars, délibération ainsi formulée :
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;
		   <i>"Messieurs ont permis au nommé CAMPENON, qui s'est fait an-</i>
		   <br>
		   <i>"noncé pour être reçu en qualité de basse-contre, de venir au</i>
		   <br>
		   <i>"choeur en habit de laïque, jusqu'à mercredi, et afin que l'on</i>
		   <br>
		   <i>"puisse juger de sa voix et de sa science dans le plain-chant, ils</i>
		   <br>
		   <i>"ont ordonné que demain il chantera seul l'Offertoire de la messe</i>
		   <br>
		   <i>"du choeur. Mesdits sieurs, se proposant de se décider, au jour</i>
		   <br>
		   <i>"indiqué ci-dessus, à l'admettre ou à le renvoyer, et autorisant</i>
		   <br>
		   <i>"M. LE COQ à lui payer demain une semaine, ainsi qu'aux autres</i><
		   <br>
		   <i>"basse-contre, comme s'il l'avait déjà gagnée."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Le 9 mars, nouvelle délibération :
		   <br>
		   <i>"D'après les épreuves auxquelles a été soumis le nommé</i>
		   <br>
		   <i>"CAMPENON, il a été reconnu qu'il était propre à remplir les fonc-</i>
		   <br>
		   <i>"tions de basse-contre dans cette église, et en conséquence, mes-</i>
		   <br>
		   <i>"sieurs ayant conclu de le recevoir aux gages ordinaires de 10</i>
		   <br>
		   <i>"liv. par semaine, payables d'avance, ils ont ordonné qu'il serait</i>
		   <br>
		   <i>"installé demain au choeur par MM les secrétaires selon l'usage,</i>
		   <br>
		   <i>"<u>mais qu'il lui serait recommandé d'aller prendre des leçons de</u></i>
		   <br>
		   <i>"<u>plain-chant à la maîtrise afin de se fortifier</u>, et de se confor-</i>
		   <br>
		   <i>"mer exactement aux obligations des commis-musiciens, dont MM les</i>
		   <br>
		   <i>"secrétaires ont été chargés de lui donner le détail, qui sera re-</i>
		   <br>
		   <i>"levé du protocole des chapitres généraux."</i>
		   <br>
		   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Il paraît que CAMPENON négligea de suivre les 
		   recommandations du chapitre, car, le 30 juin, on lui ordonna de prendre des leçons sous peine d'être 
		   renvoyé, et en attendant on décide qu'un nommé DELAFESTE devra toujours assister à l'église quand un autre 
		   chanteur, GELIN, n'y sera pas, afin qu'il y aie toujours quelqu'un pour guider CAMPENON.
		   <br>
		   <br>
           <font color="#ff0000">*****</font><font color="#666600"><i>Souvent aussi le Chapitre admettait des musiciens
		   étrangers à chanter à l'église, et on leur payait une gratification.</i></font>
		   <br>
		   <font color="#ff0000">******</font><font color="#666600"><i>Aux Archives Départementales.</i></font>
