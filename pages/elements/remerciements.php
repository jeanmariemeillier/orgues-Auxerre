<h3>Remerciements</h3>
<p>
   Cette étude s'est préparée sur le terrain. Elle n'aurait pu voir le jour sans le concours de nombreuses personnes. Je crains fort d'en oublier. Qu'elles m'en excusent. 
</p>
<p>
   Je remercie d'abord le Quotidien Régional l'YONNE REPUBLICAINE, grâce auquel j'ai pu constater, depuis mon arrivée dans le département (1966), que l'intérêt porté à l'information en matière d'orgues est certain et m'a permis de ne pas rester solitaire dans ce domaine, ainsi que tous les auteurs auxquels j'ai pu emprunter de larges extraits. 
</p>
