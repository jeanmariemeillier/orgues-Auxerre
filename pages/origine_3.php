				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;L'orgue avec son buffet fut transporté  dans l'état où il était et dès le 5 
				 février 1768 le travail était terminé. Mais une déception attendait la Compagnie : il se trouva qu'il n'était 
				 plus assez puissant à cause de son éloignement ; ce qui contraignit le facteur  à ajouter trois nouveaux jeux.
				 <BR>
				 <BR>
				 Du 8 février 1768
				 <BR>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>"M. le Chantre a représenté que lorsqu'on avoit fait marché pour 
				 racommoder l'orgue il n'étoit pas déplacé, et qu'étant actuellement hors du choeur il en étoit convenable 
				 d'en augmenter certains jeux. Messieurs après en avoir délibéré ont conclu de s'en rapporter à lui sur 
				 cette augmentation et lui ont donné tout pouvoir  de traiter avec le facteur après avoir pris du Sr 
				 PALAIS, organiste, les lumières dont il auroit besoin sur cet objet"</I>.
				 </p>
