				 <BR>
				 <div class="mondiv">ADY G 1284</div>
				 Le 13 juillet 1767, dans le Registre des Délibérations du Conseil de Fabrique :
				 <BR>
				 <p align="justify">
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>"Messieurs ont chargé Mr le Chantre d'écrire à un facteur d'orgue 
				 proposé par un religieux bénédictin et lui ont donné tout pouvoir de passer avec ledit facteur le marché 
				 de l'entreprise des ouvrages à faire à l'orgue, à tel prix qu'il jugera à propos et au meilleur prix qu'il 
				 pourra"</I>.
				 </p>
				 <BR>
				 <BR>
				 <div class="mondiv">ADY G 1803</div>
				 Du samedi 25 juillet 1767 :
				  <p align="justify">
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<i>"Mr MIGNO dit qu'il avoit fait avec le facteur d'orgue un marché de 
				 3200 livres pour racommoder celui de la Cathédrale. Messieurs l'ont remercié et autorisé à passer ledit 
				 marché"</i>.
				 </p>
				 <BR>
				 <BR>
				 <div class="mondiv">ADY G 1803</div>
				 Du lundi 21 décembre 1767 :
				  <p align="justify">
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>"Mr le Fabricien a représenté que le facteur qui travaille actuellement 
				 à réparer l'orgue de cette église lui avoit abservé que le son de l'orgue devenant plus fort par les 
				 réparations et augmentations qu'il y fait de plusieurs jeux, il n'en pouvoit résulter qu'un effet 
				 incommode pour le Choeur, s'il restoit dans le plan où il est, et qu'il conviendroit de le transporter 
				 dans quelque autre endroit de l'église..."</I>.
				 <BR>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<I>"Messieurs ont conclu que n'ayant pas pour le présent des fonds 
				 suffisant pour transporter ledit orgue au-dessus de la porte principale de leur Eglise, où est sa 
				 véritable place, il sera dès à présent placé dans la Croisée du côté du midi au-dessus de la Chapelle 
				 de St Georges à l'endroit où est actuellement un ancien horloge"</I>.
				 </p>
				 <BR>
				 <BR>
				 <div class="mondiv">ADY G 1803</div>
				  <p align="justify">
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Cet horloge avait été établi en 1401 aux frais du chapitre.
				 &#123; Mon absence totale de connaissances en paléographie des écritures des XVème, XVIème et 
				 XVIIème siècles, ainsi qu'une ignorance totale du latin, ont été un frein certain dans toutes mes 
