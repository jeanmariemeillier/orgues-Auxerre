			   <H2>Portaits imaginaires</H2>
			</CENTER>
			<FONT SIZE=5>
			   DENYS L'AUXERROIS
			</FONT>
			<FONT SIZE=4>
			   <p align="justify">
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<B>...</B> Le récit qui va suivre est une légende étrange et assez précise de 
			   retour d'un âge d'or, ou doré par la poésie (...), qui survint dans une vieille cité de la France médiévale.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;De la ville française proprement dite, dans laquelle les vestiges des siècles 
			   successifs, et quelques notes joyeuses du présent, se fondent harmonieusement en une beauté spécifique --une 
			   beauté cisalpine et septentrionale, bien distincte pourtant du massif pittoresque germanique d'Ulm, de 
			   Fribourg et d'Augsbourg, et dont Turner a trouvé l'idéal dans certaines de ses études de France, car une 
			   conjonction heureuse de rivière et de ville fait l'essentiel de sa physionomie-- la ville d'Auxerre 
			   constitue peut-être l'exemple le plus parfait à proposer au voyageur moderne. Elle est certainement, au 
			   point de vue du pittoresque, la plus expressive d'un groupe de trois villes remarquables de la région, --
			   Auxerre, Sens et Troyes,-- dont chacune se presse, comme de propos délibéré, autour de la masse cnetrale 
			   d'une immense cathédrale grise.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<B>...</B> Auxerre ! une pente légère sur la route sinueuse, et voici devant 
			   vous la plus jolie ville de france ; le large cadre des vignes, semées dans le lointain de blanches 
			   chaumières qui incitent à la marche, monte en pente douce vers l'horizon ; au-dessous c'est la boucle molle 
			   la rivière avec tous les détails de ses quais, et dominant la foule des maisons, plus abruptes, plus 
			   irrégulières qu'aucune autre église de France, les vastes masses à tuiles rousses de Saint-Germain, de 
			   Saint-Pierre et de la cathédrale Saint-Etienne. Sujet tout fait pour cet artiste rare qu'est un bon peintre 
			   de monuments, s'il comprend la valeur des masses et des lignes, des masses énormes et des lignes délicates.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<B>...</B> Pour bien remplir les heures d'une de ces après-midi où la pluie, 
			   tôt venue, interdisait toute promenade, je me dirigeai vers la boutique d'un vieux brocanteur. Ce n'était 
			   pas de ces exhibitions monotones copiées sur celles des magasins parisiens, d'un fond de boutique que l'on 
			   a vu partout, mais une collection choisie de curiosités véritables. On reconnaissait la tradition de la 
			   province en maintes reliques de la vie domestique du siècle précédent, et en nombreux bijoux des époques 
			   antérieures, tirés des vieilles églises et des maisons religieuses du voisinage.
			   <BR>
			   Il y avait, entre autres, un important et éclatant fragment de vitrail, qui pouvait provenir de la 
			   cathédrale même. D'une rare qualité de couleur et de dessin, il représentait un personnage difficile à 
			   rapporter à aucun type connu, et faisait manifestement partie d'une série. A mes instantes questions sur ce 
			   qu'il avait pu advenir du reste, le vieillard répondit que l'on n'en savait rien, mais ajouta que le curé 
			   d'un village voisin possédait toute une suite de tapisseries évidemment destinées à une église, et qui 
			   représentait l'ensemble du sujet dont le vitrail en question ne formait qu'un détail.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Le lendemain, je me rendis aupresbytère, petite bâtisse gothique toute proche 
			   de l'église du village, et partie sans doute d'un ancien chateau... Le prêtre mit beaucoup de courtoisie 
			   à me montrer ses tapisseries dont certaines, pendues au mur du parloir et de l'escalier, servaient de fond aux 
			   autres curiosités de sa collection. Incontestablement, vitrail et tapisseries représentaient le même sujet. 
			   On retrouvait, sur les unes et sur l'autre, les mêmes instruments de musique, pipeaux, cymbales et trompettes 
			   en forme de roseaux. <FONT COLOR=#FFCCFF><B>Et il y était question d'un orgue en construction, instrument de tout point semblable, 
			   mais sur une plus grande échelle, à celui qui se trouvait dans la bibliothèque du vieux prêtre, et 
			   demeurait maintenant presque sans voix, tandis que sur certains des panneaux tissés, les auditeurs 
			   semblaient enchantés par la musique de l'instrument qu'ils acclamaient même d'enthousiasme.</B></FONT> Et partout, 
			   entre les complexités délicates de toutes ces scènes, semblait régner une sorte de fole véhémence, danses 
			   vertigineuses, bondissements de faunes, et éternels festons de vigne... <FONT COLOR=#FFCCFF><B>C'était le constructeur de l'orgue 
			   lui-même, jeune homme blond et fleuri que l'on voyait tantôt  presque nu parmi les pampres, tantôt emmitouflé 
			   de fourrures, pour se protéger du froid, tantôt en robe de moine, mais qui laissait toujours une impression de 
			   réalisme frappant et en rapport singulier avec le caractère des rues d'Auxerre.</B></FONT> De qui s'agissait-il ? 
			   C'était evidemment, malgré sa grâce et la richesse du décor, un être douloureux et tourmenté. Avec toute la 
			   beauté classique d'un dieu païen, il avait souffert des tortures sans doute inconnues à ces dieux... C'est 
			   avec cette idée, en recourant aux notes trouvées dans l'intéressante bibliothèque du prêtre, touchant 
			   l'histoire des travaux accomplis dans la cathédrale à l'époque de son achèvement, et à la suite d'études 
			   répétées des vieilles tapisseries, que je sentis la vérité se faire jour dans mon esprit.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Vers le milieu du XIIIème siècle, tout le gros oeuvre de la cathédrale 
			   Saint-Etienne était achevé ; il ne restait plus à élever que la grande tour, et à pousser tout ce travail 
			   final de décoration qu'une seule génération ne pensait pas devoir mener à bien. Et cependant, certaines 
			   circonstances, un peu confuses encore déterminèrent un achèvement rapide, et quasi instantané des 
			   travaux, qui n'en gardèrent pas moins un caractère remarquable de richesse et de grâce. De cette décoration 
			   une bonne partie fut détruite, ou transférée en d'autres lieux ; mais il en subsiste des vestiges 
			   somptueux sous forme de vitraux et surtout de sculptures très délicates des portails de l'ouest, taillées 
			   dans la belle pierre dure de Tonnerre <I>(Yonne NdA)</I>dont le temps n'a fait que dorer la surface...
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<B>...</B> Une étrange coutume se perpétuait à Auxerre. Le dimanche de Pâques, 
			   les chanoines jouaient solennellement à la balle en plein centre de la vaste église. <I>(NdA. Légère erreur, 
			   c'est au niveau de la première travée, là où était le labyrinthe -depuis disparu- proche des grandes portes 
			   que se situait la fête, d'après les textes d'époque)</I> Après vêpres, au lieu de reconduire l'évêque à son 
			   palais, ils se rendaient en cortège dans la nef, au milieu de l'affluence des fidèles qui se rangaient de 
			   part et d'autre pour les regarder. Retroussant à demi leurs soutanes, les clercs attendaient leur tour en 
			   silence ; le chef des enfants de choeur lançait la balle en l'air, aussi haut qu'il le pouvait, sous la 
			   voûte de la nef principale, et ses camarades s'eforçaient de la rattraper ; celui qui y avait réussi la 
			   relançait de la main ou du pied vers l'un des chantres majestueux, des chapelains ou des chanoines même, 
			   qui s'adonnaient au jeu avec tout le décorum d'une cérémonie ecclésiastique. C'est au moment même où les 
			   prêtres se relançaient la balle avec tant de solennité que Denys --Denys l'Auxerre comme on l'appela plus 
			   tard-- fit sa première apparition. Bondissant au milieu des enfants intimidés, il fit de la cérémonie une 
			   aprtie véritable. Les enfants se mirent à jouer comme des enfants, les hommes comme des fous, et tous avec 
			   un bel enthousiasme qui se communiqua bientôt aux clercs, puis aux spectateurs mêmes. Le vieux doyen du 
			   chapitre, protonotaire de sa Sainteté releva un peu sa soutane et, s'avançant avec une vivacité stupéfiante 
			   comme s'il eut été soudain allégé du poids de ses quantre vingts ans, lança du pied la balle au vénérable 
			   prêcheur capitulaire, qui sut se montrer à la hauteur de la situation. Après quoi les laïques, incapables 
			   de demeurer plus longtemps inactifs, se mêlèrent au jeu avec des cris de joie plus ou moins étouffés, et 
			   prolongèrent la partie jusqu'à ce que la pénombre de l'église empêchât de dsitinguer la balle.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;<B>...</b> On éprouvait, par-dessus tout autre, à lépoque, le désir de créer 
			   les instruments d'une musique sacrée plus ample et plus variée que l'on en avait pu faire encore, d'une 
			   musique qui put traduire toute l'expansion des âmes dorénavant muries. Auxerre, dès cette époque, était, 
			   comme elle le fut plus tard, renommée pour sa musique liturgique. Ce fut Denys à qui revint, en définitive, 
			   l'idée de combiner en une masse musicale élargie tous les instruments alors en usage. Comme l'ancien dieu 
			   du vin, il avait aimé et pratiqué la musique des pipeaux sous toutes leurs formes. Ici encore se 
			   manifestèrent trois époques ou "modes" de son influence : d'abord, c'est le mode simple et pastoral, la note 
			   familière du chalumeau, pareille au souffle de la brise sur les campagnes lointaines ; puis le fracas 
			   frénétique et sauvage, effroi des gens rassis, qui avait affolé les coeurs sensibles. <FONT COLOR=#FFCCFF><B>Enfin, il 
			   s'efforça de faire concourir toutes ces formes à de plus douces fins, et la construction du premier orgue 
			   devint le véritable livre de sa vie, et traduisit toute sa nature dans ses délices et ses détresses. En de 
			   longues journées heureuse de vent et de soleil, le frère apparemment simple d'esprit chercha et trouva sur 
			   les berges de la rivière les espèces de roseaux nécessaires. Sous sa direction, les charpentiers 
			   construisent les gros tuyaux de bois pour déchaîner les tonnerres, cependant que de petites flûtes de 
			   carton imitaient le son de la vois humaine et répondaient aux notes victorieuses des longues trompettes 
			   de métal. Parfois, aux gens qui entendaient nuit après nuit ces sons discordants, tout ce bruit faisait 
			   l'effet d'un travail de dément, mais de temps en temps c'étaient les bribes d'une musique incontestablement 
			   nouvelle qui les arrachaient à leur sommeil et les émerveillaient. Le roseau, triomphait, à tous ses degrés 
			   de puissance, assoupli, unifié, assagi. Mais sur les panneaux décorés de l'orgue, Appolon, lyre en main, et 
			   maître des cordes, semblait écouter sans bienveillance la musique des roseaux et sentir renaître la jalousie 
			   qui avait si cruellement fait meurtrir Marsyas.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;... Cependant les vents de son orgue étaient prêts à souffler, mais ce n'est 
			   pas sans peine qu'il obtint du chapitre l'autorisation d'éprouver leur puissance à l'occasion d'une grande 
			   cérémonie. Voici en quelles circonstances. On attendait à Auxerre un hôte de marque. En récompense de 
			   quelques service rendu au chapitre, le sire de Chastellux se paraît héréditairement de la dignité de chanoine 
			   de l'église. Au jour de sa réception il se présentait à l'entrée du choeur, revêtu, par-dessus son attirail 
			   guerrier, du surplis et de l'amict. Le vieux comte de Chstellux étant récemment décédé, son héritier avait 
			   annoncé sa visite, pour se prévaloi, selon la coutume, de son privilège ecclésiastique. Il y avait eu 
			   longtemps rivalité entre les maisons d'Auxerre et de Chastellux, mais, en cette occurence, une offre de 
			   réconciliation sous la forme d'une demande de la main de dame Ariane.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Le beau jeune homme se présenta et, dûment équipé, fut, aux vêpres, installé 
			   dans sa stalle, en présence de l'évêque. C'est alors qu'avec des sentiments de délices variés, la foule 
			   entendit pour la première fois la musique de l'orgue rouler sur sa tête. Mais l'exécutant et constructeur 
			   de l'instrument passa inaperçu, l'ancien favori ne retrouva point sa popularité. La cérémonie fut suivie 
			   d'une fête civile par laquelle Auxerre accueillait son futur maître.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;... Ainsi s'expliquait la figure du vitrail et Denys me paraissait avoir 
			   réellement vécu à Auxerre. Par des jours d'une atmosphère spéciale où les traces du moyen âge ressortent 
			   comme les vieilles marques sur les pierres lavées par la pluie, je me figurais avoir réellement vu le 
			   personnage douloureux, avoir rencontré dans les rues Denys l'Auxerrois.</B></FONT>
			   <BR>
			   </P>
			   <P ALIGN="RIGHT">
			   W.P. 1887
