				 <BR>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Il ressort de cette remarque, que je fus dans l'impossibilité de trouver des 
				 documents relatifs à cet(te) horloge. (début du XVè s.)
				 <BR>
				 &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;(Adrien LEPINE, arrivé à Paris en 1758, semble trouver du travail à l'ombre de 
