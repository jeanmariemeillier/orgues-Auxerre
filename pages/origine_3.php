			   Jacques Amyot trouvait refuge dans son ultime consolation, le seul authentique plaisir de sa vie : la musique, sa passion !
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Chaque soir, très tard, il pénétrait à pas lents dans son immense cathédrale déserte, 
			   s'asseyait dans son fauteuil épiscopal et inclinait la tête entre ses mains. Sa méditation s'effilochait lentement en un 
			   songe de plus en plus vaporeux puis son esprit se dissolvait dans la pénombre.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Encadrés de chandeliers, des chanoines musiciens attendaient au pupitre de l'orgue et, très 
			   doucement, d'abord pour leur seul auditeur, libéraient les premières harmonies de leur concert quotidien. Puis les volumes 
			   s'enflaient, les contrastes s'amplifiaient, et la grande voûte résonnait bientôt des prouesses de l'instrument. Attentif à 
			   leurs claviers, à leurs manettes, à leurs pédaliers, les organistes distillaient pour Jacques Amyot les rumeurs et déversaient 
			   les tonnerres de leur myriade de soufflets et de tuyaux.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Les grandes orgues de la cathédrale étaient un fantastique monument sonore, un des plus 
			   prodigieux chefs-d'oeuvre de l'art et de la technique musicale. Des générations d'artisans aux doigts de fée s'étaient 
			   succédé pour doter notre cité d'un instrument que tout l'Occident nous enviait.<SUP><FONT size=2 color="#FF0000">(1)</FONT>
			   </SUP> Ses sonorités étaient extraordinaires ; il soupirait d'imperceptibles nuances mélodiques qui s'évaporaient 
			   jusque dans les moindres recoins de l'immense bâtiment ; quand il déchaînait sa puissance, le sanctuaire entier vibrait et 
			   tressaillait. L'orgue savait soupirer et chuchoter, exhaler des grondements caverneux, ou exploser en une cataracte 
			   d'effrayants rugissements. C'était un assemblage gigantesque et délicat de... sept mille tuyaux, certains aussi fins qu'une 
			   paille et d'autres des troncs monstrueux !<SUP><FONT size=2 color="#FF0000">(2)</FONT></SUP> 
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Chaque soir, dans sa nuit trouée seulement de la lueur de quelques bougies, seul, Jacques 
			   Amyot laissait ainsi son esprit se dissoudre dans les murmures et les fracas des grandes orgues de la cathédrale. Chaque soir, 
			   jusqu'à cette aube fatale du 28 septembre 1567...
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;L'évêque avait dû partir en voyage, quelque part dans le royaume. La ville et la campagne 
			   respiraient la sérénité, dans une automne d'"une grande douceur". Dans la nuit du 27 au 28, par surprise et avec la 
			   complicité de quelques huguenots de la ville, Auxerre fut conquis par une armée protestante.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Au matin, les vainqueurs convergèrent vers la cathédrale et l'évêché pour les mettre à sac. 
			   A grands coups de pic et de masse, ils jetèrent bas les statues, défoncèrent les dalles pour exhumer les évêques qui 
			   dormaient là de leur dernier sommeil, et jeter leurs cadavres aux quatre vents. Ils brisèrent les vitraux.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Puis ils s'attaquèrent aux grandes orgues, leur arrachant des tintements de douleurs sous 
			   une grêle de gravats. On trouva vite des échelles et les soudards se hissèrent là-haut pour frapper rageusement. Les 
			   tuyaux les plus frêles dégringolèrent comme une gerbe déliée ; il fallut desceller les plus gros, qui basculèrent 
			   lentement et explosèrent sur les dalles à grand fracas. Le monument musical, défoncé, effondré pan à pan, hurlait à 
			   chaque choc comme une bête à l'agonie, submergeant de son tonnerre le rire des soldats.
			   <BR>
			   &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Quelques mois plus tard, Auxerre se révolta et chassa les protestants, et Jacques Amyot 
			   revint. C'est à grand peine qu'il put se frayer un chemin dans sa cathédrale jonchée de gravats. Le coeur serré de 
			   désespoir, il resta de longues heures à contempler l'enchevêtrement des morceaux des sept mille tuyaux éventrés, tristes 
			   épaves désormais silencieuses des grandes orgues à jamais disparues.
