				 propreté, mais à cause des différents bois qui le composent, dont partie est vieux et l'autre neuf. 
				 Messieurs après en avoir délibéré ont conclu à ce que ledit buffet d'orgue soit mis en couleur de détrempe 
				 seulement, avec un vernis par dessus et non à l'huile"</I>.
				 </p>
				 <BR>
				 <BR>
